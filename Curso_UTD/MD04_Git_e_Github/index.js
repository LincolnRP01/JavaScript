/*
Principais comandos do Git

git init: inicia um repositório local na pasta designada.

git clone <url>: copia um repositório remoto para dentro da pasta designada.

git add <nome do arquivo> : adiciona um arquivo específico para o monitoramento do git.

git add . ou git add * : adiciona todos os arquivos de uma vez só.

git diff : verifica as modificações feitas.

git config --list : ver configurações de usuário.

git config --global user.name "Usuário nome" : configura o nome de usuário.

git config --global user.email "osvaldo@email.com" : configura o email de usuário.

git commit -m <mensagem> : commita todas as mudanças adicionadas junto a uma mensagem.

git log : mostra o registro de todos os commits. 

git remote add origin <url> : adiciona a url de um repositório remoto no seu repositório local.

git remote remove origin : remove a url do repositório remoto.

git push origin <nome da branch> : envia os arquivos para o repositório remoto fazendo as modificações na branch selecionada.

git pull <url> <branch> : trás do repositório remoto todas as modificações feitas por lá.

git branch : listar as branches existentes.

git branch <nome da branch> : criar uma branch nova.

git branch -d <nome da branch> : deletar uma branch específica.

git checkout -b <nome da branch> : cria e já muda para a branch criada.

git merge <nome da branch> : adicionar as mudanças feitas em outra branch para a atual.


*/